    private void btnUploadImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadImageActionPerformed
        javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
    fileChooser.setDialogTitle("Select an Image");
    fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Image Files", "jpg", "png", "jpeg", "bmp"));

    int result = fileChooser.showOpenDialog(this);
    if (result == javax.swing.JFileChooser.APPROVE_OPTION) {
        java.io.File selectedFile = fileChooser.getSelectedFile();
        try {
            studentImageBytes = java.nio.file.Files.readAllBytes(selectedFile.toPath()); // store the image in memory

            javax.swing.ImageIcon imageIcon = new javax.swing.ImageIcon(
                new javax.swing.ImageIcon(studentImageBytes).getImage()
                .getScaledInstance(lblImage.getWidth(), lblImage.getHeight(), java.awt.Image.SCALE_SMOOTH)
            );
            lblImage.setIcon(imageIcon);
            lblImage.setText(null);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Image load failed: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_btnUploadImageActionPerformed

