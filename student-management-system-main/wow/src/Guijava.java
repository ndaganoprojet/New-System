    private void jbtnReadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnReadActionPerformed
           try (Connection conn = DBConnection.connect()) {
        String sql = "SELECT * FROM Registration WHERE RegID = ?";
        var ps = conn.prepareStatement(sql);
        ps.setString(1, jtxtRegis.getText());
        var rs = ps.executeQuery();
        if (rs.next()) {
            jtxtName.setText(rs.getString("Name"));
            jtxtFaculty.setText(rs.getString("Faculty"));
            jtxtProject.setText(rs.getString("Project"));
            jtxtContact.setText(rs.getString("Contact"));
            jtxtEmail.setText(rs.getString("Email"));
            
            byte[] imgBytes = rs.getBytes("Image");
            if (imgBytes != null && imgBytes.length > 0) {
                java.awt.Image img = javax.imageio.ImageIO.read(new java.io.ByteArrayInputStream(imgBytes));
                if (img != null) {
                    java.awt.Image scaledImg = img.getScaledInstance(lblImage.getWidth(), lblImage.getHeight(), java.awt.Image.SCALE_SMOOTH);
                    lblImage.setIcon(new javax.swing.ImageIcon(scaledImg));
                    lblImage.setText(null);
                } else {
                    lblImage.setIcon(null);
                    lblImage.setText("Invalid Image Format");
                }
            } else {
                lblImage.setIcon(null);
                lblImage.setText("No Image");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Record not found.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
      
    }//GEN-LAST:event_jbtnReadActionPerformed

    private void jbtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAddActionPerformed
          try (Connection conn = DBConnection.connect()) {
        String contact = jtxtContact.getText().trim();
        if (!contact.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Contact must contain only digits.");
            return;
        }
        String sql = "INSERT INTO Registration (RegID, Name, Faculty, Project, Contact, Email, Image) VALUES (?, ?, ?, ?, ?, ?, ?)";
        var ps = conn.prepareStatement(sql);
        ps.setString(1, jtxtRegis.getText().trim());
        ps.setString(2, jtxtName.getText().trim());
        ps.setString(3, jtxtFaculty.getText().trim());
        ps.setString(4, jtxtProject.getText().trim());
        ps.setString(5, contact);
        ps.setString(6, jtxtEmail.getText().trim());
        ps.setBytes(7, studentImageBytes);
        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Record Added Successfully!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jbtnAddActionPerformed
    
    private void jbtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnUpdateActionPerformed
       try (Connection conn = DBConnection.connect()) {
    String sql = "UPDATE Registration SET Name=?, Faculty=?, Project=?, Contact=?, Email=?, Image=? WHERE RegID=?";
    var ps = conn.prepareStatement(sql);
    ps.setString(1, jtxtName.getText().trim());
    ps.setString(2, jtxtFaculty.getText().trim());
    ps.setString(3, jtxtProject.getText().trim());
    ps.setString(4, jtxtContact.getText().trim());
    ps.setString(5, jtxtEmail.getText().trim());

    if (studentImageBytes != null) {
        ps.setBytes(6, studentImageBytes);
    } else {
        ps.setNull(6, java.sql.Types.BINARY);
    }

    ps.setString(7, jtxtRegis.getText().trim());

    int rowsUpdated = ps.executeUpdate();
    if (rowsUpdated > 0) {
        JOptionPane.showMessageDialog(this, "Record Updated Successfully!");
    } else {
        JOptionPane.showMessageDialog(this, "No record found with this Registration ID.");
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}


    }//GEN-LAST:event_jbtnUpdateActionPerformed

    private void jbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDeleteActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        try (Connection conn = DBConnection.connect()) {
            String sql = "DELETE FROM Registration WHERE RegID=?";
            var ps = conn.prepareStatement(sql);
            ps.setString(1, jtxtRegis.getText().trim());
            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Record Deleted Successfully!");
                btnResetActionPerformed(null); // Clear form
            } else {
                JOptionPane.showMessageDialog(this, "No record found to delete.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jbtnDeleteActionPerformed

